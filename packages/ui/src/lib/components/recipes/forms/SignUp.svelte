<script lang="ts">
	import {UiStatus, UiTextContext, type FormProps} from '$types'
	import {onMount} from 'svelte'
	import {enhance} from '$app/forms'
	import Button from '$lib/components/blocks/buttons/Button.svelte'
	import Feedback from '$lib/components/blocks/global/Feedback.svelte'
	import Input from '$lib/components/blocks/inputs/Input.svelte'
	import InputPassword from '$lib/components/blocks/inputs/InputPassword.svelte'
	import AjvValidator from '$lib/utils/validate-ajv.svelte.js'

	let {
		id = 'sign-up-form',
		method = 'POST',
		title = 'Sign Up',
		description = 'A sample signup form',
		actionPath, // 'ui'
		formaction = 'signup', // 'signup'
		redirect,
		layout,
		container,
		depth = 2, // <h*> elenent depth
		size,
		color = 'primary',
		variant = 'default',
		asset = 'log',
		align = 'center',
		background = 'contrast',
	}: FormProps = $props()

	let boundForm: HTMLFormElement | undefined = $state()
	let formData: FormData | undefined = $state()
	let validator: AjvValidator = new AjvValidator('SignUpValidationFunction')
	let disabled: boolean | undefined = $state(undefined)
	let successPlaceholder: boolean = $state(false)

	let fields = $state({
		sample_email: '',
		sample_password: '',
		confirm_password: '',
	})

	let messages = $derived({
		sample_email: {
			[UiStatus.error]: validator.getFieldErrors('sample_email'),
		},
		sample_password: {
			[UiStatus.error]: validator.getFieldErrors('sample_password'),
		},
		confirm_password: {
			[UiStatus.error]: validator.getFieldErrors('confirm_password'),
		},
	})

	$effect(() => {
		disabled = validator.formHasErrors()
	})

	/**
	 * For the form to work:
	 * - `formaction` and `redirect` must be defined (`action` must be defined)
	 * - The `<form>` element must set `action` to `?/${action}` instead of `undefined` (see `action` prop in form element)
	 */
	let action = $derived(
		formaction && redirect ? `${formaction}&redirectTo=${redirect}` : undefined,
	)

	function handleSubmit(event: Event) {
		event.preventDefault()

		// Submit the form here
		// ...
		if (!validator.errors.length) {
			successPlaceholder = true
		}
	}

	function handleFocus(event: Event) {
		validator.changeInput(event)
	}

	function handleBlur(event: Event) {
		validator.validateInput(event)
	}

	function handleInput(event: Event) {
		validator.changeInput(event)
	}

	onMount(() => {
		if (boundForm) {
			formData = new FormData(boundForm)
			//  Default function generated by the `@fat-fuzzy/validation` package for demo
			validator.init(formData)
		}
	})
</script>

<div class={`card:${size} ${background} l:${container}:${size}`}>
	{#if successPlaceholder}
		<Feedback status={UiStatus.success} context={UiTextContext.form}>
			Form submitted!
		</Feedback>
	{:else}
		<form
			{method}
			class={`l:stack:${size} card:${size}`}
			action={action && actionPath ? `${actionPath}?/${action}` : undefined}
			use:enhance
			bind:this={boundForm}
			onsubmit={handleSubmit}
		>
			{#key validator}
				<header class={`l:stack:${size} text:${align} ${asset}`}>
					<svelte:element this={`h${depth}`}>{title}</svelte:element>
					<p class={`font:${size}`}>{description}</p>
				</header>
				<Input
					id="email"
					type="email"
					name="sample_email"
					label="Email"
					required
					bind:value={fields.sample_email}
					{size}
					onfocus={handleFocus}
					onblur={handleBlur}
					oninput={handleInput}
					feedback={messages.sample_email}
				/>
				<InputPassword
					id="password"
					name="sample_password"
					label="Password"
					required
					bind:value={fields.sample_password}
					{size}
					onfocus={handleFocus}
					onblur={handleBlur}
					oninput={handleInput}
					feedback={messages.sample_password}
				/>
				<InputPassword
					id="password"
					name="confirm_password"
					label="Confirm Password"
					required
					bind:value={fields.confirm_password}
					{size}
					onfocus={handleFocus}
					onblur={handleBlur}
					oninput={handleInput}
					feedback={messages.confirm_password}
					disabled={messages.sample_password.error?.length ? true : undefined}
				/>
				<Button
					id="button-submit-signup"
					{variant}
					{color}
					name="submit"
					{disabled}
				>
					Submit
				</Button>
			{/key}
		</form>
	{/if}
</div>
